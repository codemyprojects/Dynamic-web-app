    <h3>Disaster Risk Reduction Project</h3>
    <p style="text-align:justify; color:#003333;"> In North Dhading, there is high risk of disaster because of its remote geographical location and lack of awareness. In the 2015 earthquake infrastructure was totally destroyed and the Disaster Risk Reduction Project is looking at disaster prevention and risk reduction including prevention activities like light search and rescue training, first aid training, emergency shelter home construction, climate change and environment protection activities.</p>
