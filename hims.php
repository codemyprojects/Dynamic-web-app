    <h3>Community Infrastructure and Hydropower Project</h3>
   
   <p style="text-align:justify; color:#003333;">In the earthquake of 2015, in Ree and Jharlang, almost all drinking water systems, and toilets (individual and public) have been destroyed and community people have been suffering from various health related problems resulting in hygiene problems and dropouts from schools. HIMS Nepal has started to work on the Water, Sanitation and Hygiene (WASH) programme and construction and reconstruction of different schemes for communal use.</p>
    
