    <h3>Protection Project</h3>
   
   <p style="text-align:justify; color:#003333;">The protection programme focuses on children, particularly those who have become homeless, and senior citizens who face difficulties surviving. HIMS has implemented activities for child protection which support peer educators, dysfunctional child clubs and teaching material supply.</p>
    
  </div>

  <div class="column" style="background-color:#FFFFFF; margin-top:120px">
    
    <h3>Psycho social and Trauma Healing</h3>
   
   <p style="text-align:justify; color:#003333;">Many people have experienced trauma from the earthquake events. HIMS Nepal is involved in screening them for trauma and awareness activities for school children. Sports materials have been supplied to schools and try to minimize children's trauma through enjoyable activities such as sports, dance, singing, games and storytelling.</p>
    
  </div>
  
    <div class="column" style="background-color:#FFFFFF; margin-top:145px">
    
    <h3>Good Governance</h3>
   
   <p style="text-align:justify; color:#003333;">Ward Citizen Forum coordinators and social mobilisers have received training in good governance. Through health posts they have made an action plan in order to address the queries and responses of the community to become more accountable. A feedback mechanism has been established with suggestion boxes installed in field and district offices as well as hoarding boards. The Local Development Officer has encouraged other NGOs to establish this feedback system in their working areas.</p>
    
  </div>

